/**
 * Searches a text block for medical terms and returns matched glossary details.
 */
export const decodeText = (text) => {
  if (!text) return [];
  const normalized = text.toLowerCase();
  const matches = [];

  Object.keys(dictionary).forEach((key) => {
    // Check if the key term appears as a word boundaries match
    // To support terms like ANA, TPO, as well as substrings like dysmenorrhea
    const regex = new RegExp(`\\b${key}\\w*\\b`, 'gi');
    if (regex.test(normalized)) {
      matches.push(dictionary[key]);
    }
  });

  return matches;
};
